package com.syntax.class09;

public class NestedPatering {
public static void main(String args[]) {
	for(int i=5;i>=1;i--) {
	for(int a=5;a>=1;a--) {
	System.out.print(a);
}
	System.out.println();
}
	System.out.println("+++++++++++");
	for(int i=5;i>=1;i--) {
		for(int a=5;a>=1;a--) {
		System.out.print(i);
	}
		System.out.println();
}
}
}
