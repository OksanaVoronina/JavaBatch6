public class Repl {
	public static void main(String args[]){
	   
	   for(int a=7;a>=1;a--) {
		   for(int b=1;b<=a;b++) {
			   System.out.print(b);
	   }
		   System.out.println(" ");
	   }
		
		for(int i=2;i<7;i++){
	      for(int x=1;x<=i;x++){
	        System.out.print(x);
	      }
	      System.out.println(" ");
}
	}
}

